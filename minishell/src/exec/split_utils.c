#include "pipex.h"

void	avoid_quote(const char *str, size_t *i)
{
	if (!str[(*i)])
		return ;
	if (str[(*i)] == '\'')
	{
		(*i)++;
		while (str[(*i)] && str[(*i)] != '\'')
			(*i)++;
	}
	if (str[(*i)] == '"')
	{
		(*i)++;
		while (str[(*i)] && str[(*i)] != '"')
			(*i)++;
	}
}
